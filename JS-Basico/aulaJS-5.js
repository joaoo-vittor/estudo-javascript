// variaveis

let nome = 'João'; // variavel inicializada;

console.log('Meu nome é', nome);


let nome2; // variavel declarada;

console.log(nome2);

nome2 = 'joao';
console.log(nome2);

nome2 = 'Outro nome';
console.log(nome2);

/*

1 - Não pode criar variaveis com palavras reservadas;
2 - Deve ter nome significativos;
3 - Não pode começar com numeros;
4 - Não pode conter espaço ou traços;
5 - São Case-Sensitive;
6 - Não pode redeclarar variaveis com let;
7 - Não UTILIZE VAR, UTILIZE LET;
*/
